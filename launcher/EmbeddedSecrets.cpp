#include "EmbeddedSecrets.h"

#include <QByteArray>

namespace {
constexpr char kKey = static_cast<char>(0x5A);

QString decode(const char* data, int length)
{
    QByteArray out;
    out.resize(length);
    for (int i = 0; i < length; ++i)
        out[i] = static_cast<char>(data[i] ^ kKey);
    return QString::fromLatin1(out);
}

static const char kMetaBaseUrlEnc[] = {
    0x32, 0x2e, 0x2e, 0x2a, 0x29, 0x60, 0x75, 0x75, 0x37, 0x3f, 0x2e, 0x3b, 0x74, 0x2a, 0x28, 0x33,
    0x29, 0x37, 0x36, 0x3b, 0x2f, 0x34, 0x39, 0x32, 0x3f, 0x28, 0x74, 0x35, 0x28, 0x3d, 0x75, 0x2c,
    0x6b, 0x75,
};

static const char kLegacyFmlLibsUrlEnc[] = {
    0x32, 0x2e, 0x2e, 0x2a, 0x29, 0x60, 0x75, 0x75, 0x3c, 0x33, 0x36, 0x3f, 0x29, 0x74, 0x2a, 0x28,
    0x33, 0x29, 0x37, 0x36, 0x3b, 0x2f, 0x34, 0x39, 0x32, 0x3f, 0x28, 0x74, 0x35, 0x28, 0x3d, 0x75,
    0x3c, 0x37, 0x36, 0x36, 0x33, 0x38, 0x29, 0x75,
};
}  // namespace

QString EmbeddedSecrets::metaBaseUrl()
{
    return decode(kMetaBaseUrlEnc, static_cast<int>(sizeof(kMetaBaseUrlEnc)));
}

QString EmbeddedSecrets::legacyFmlLibsUrl()
{
    return decode(kLegacyFmlLibsUrlEnc, static_cast<int>(sizeof(kLegacyFmlLibsUrlEnc)));
}
